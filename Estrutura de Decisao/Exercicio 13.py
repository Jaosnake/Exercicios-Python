dia = int(input('Digite o dia da semana: '))
if dia == 1:
    print('Dia 01 - Domingo')
elif dia == 2:
    print('Dia 02 - Segunda-Feira')
elif dia == 3:
    print('Dia 03 - Terça-Feira')
elif dia == 4:
    print('Dia 04 - Quarta-Feira')
elif dia == 5:
    print('Dia 05 - Quinta-Feira')
elif dia == 6:
    print('Dia 06 - Sexta-Feira')
elif dia == 7:
    print('Dia 07 - Sábado')
else:
    print('Valor inválido')
