n1 = float(input("Digite a primeira nota do bimestre: "))
n2 = float(input("Digite a segunda nota do bimestre: "))
n3 = float(input("Digite a terceira nota do bimestre: "))
n4 = float(input("Digite a quarta nota do bimestre: "))

media = (n1 + n2 + n3 + n4) / 4
print(f'A média das notas digitadas é: {media:.2f}')
