# Produto de um número é a sua multiplicação com outro número real

x = int(input("Digite um número inteiro: "))
y = int(input('Digite outro número inteiro: '))
z = float(input('Digite um número inteiro ou com decimais: '))

produto = (x*2)*(y/2)
soma = (x*3) + z
cubo = (z**3)

print (f'\nO produto do dobro do primeiro com metade do segundo: {produto}')
print (f'A soma do triplo do primeiro com o terceiro: {soma}')
print (f'O terceiro elevado ao cubo: {cubo}')